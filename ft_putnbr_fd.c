#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == 0)
		ft_putchar_fd(n + '0', fd);
	else if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else if (n < 10 && n > 0)
		ft_putchar_fd(n + '0', fd);
	else if (n < 0)
	{
		n = -n;
		ft_putchar_fd('-', fd);
		ft_putnbr_fd(n, fd);
	}
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
